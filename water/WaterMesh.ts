/**
 * Parameters for a single Gerstner wave layer.
 *
 * - `amplitude`: Peak displacement from rest level (meters).
 * - `wavelength`: Distance between successive crests (meters).
 * - `speed`: Propagation speed (meters/second).
 * - `direction`: 2D unit vector `(dx, dz)` on the XZ plane.
 * - `steepness`: Shape parameter `q ∈ [0, 1]`.  `0` degenerates to a
 *   linear sine wave; values near `1` produce sharp, trochoidal crests.
 */
export interface GerstnerWaveConfig {
  amplitude: number;
  wavelength: number;
  speed: number;
  direction: [number, number];
  steepness: number;
}

/**
 * Configuration for the ocean mesh.
 */
export interface WaterMeshConfig {
  size: number;
  segments: number;
  waves: GerstnerWaveConfig[];

  /**
   * HDR environment map generated by PMREMGenerator.
   * Used for physically based reflections.
   */
  envMap?: Texture | null;

  /**
   * Spherical harmonic irradiance map for diffuse environment lighting.
   * When omitted, the shader falls back to a simple ambient term.
   */
  irradianceMap?: Texture | null;

  /**
   * Precomputed BRDF lookup texture (2D) for integrating the GGX
   * distribution over the environment map.  Generated offline or via
   * a dedicated BRDF shader.  When omitted, a closed-form approximation
   * is used in the shader.
   */
  brdfLUT?: Texture | null;

  /**
   * Surface roughness for the GGX distribution.
   * `0` = mirror-smooth, `1` = completely diffuse.
   * Water is typically 0.1–0.4 depending on wind.
   */
  roughness?: number;

  /**
   * Metalness factor.  Water is a dielectric so this stays `0`.
   * Kept for material compatibility with the standard PBR workflow.
   */
  metalness?: number;

  /**
   * Index of refraction for the Fresnel calculation.
   * Water ≈ 1.33, but can be tweaked for artistic effect.
   */
  ior?: number;

  /**
   * Deep water colour (troughs).
   */
  deepColor?: [number, number, number];

  /**
   * Shallow / surface colour (crests).
   */
  shallowColor?: [number, number, number];

  /**
   * Foam colour applied at wave crests.
   */
  foamColor?: [number, number, number];

  /**
   * Threshold (in elevation space) above which foam appears.
   */
  foamThreshold?: number;

  /**
   * Divergence threshold for wave-breaking detection.
   * When the accumulated horizontal velocity divergence exceeds this
   * value, foam is generated.  Higher values mean less breaking foam.
   * Typical range: 0.01 – 0.2.
   */
  foamBreakingThreshold?: number;

  /**
   * Spatial frequency of the procedural foam noise.
   * Larger values produce smaller, denser foam cells.
   * Typical range: 2 – 20.
   */
  foamNoiseScale?: number;

  /**
   * Drift speed of the foam noise pattern (world units/sec).
   * Creates the illusion of foam being advected by currents.
   * Typical range: 0.5 – 5.
   */
  foamNoiseDrift?: number;

  /**
   * Distance to the nearest shoreline (world units).
   * When set, foam accumulates within `shoreFoamWidth` meters of
   * the shore boundary.  Omit or set to Infinity to disable.
   */
  shoreDistance?: number;

  /**
   * Width of the foam band along the shoreline.
   */
  shoreFoamWidth?: number;

  /**
   * Subsurface scattering approximation colour.
   * Simulates light that penetrates the surface and exits nearby.
   */
  sssColor?: [number, number, number];

  /**
   * Scale of the SSS effect.  Larger values let light penetrate deeper.
   */
  sssScale?: number;
}

